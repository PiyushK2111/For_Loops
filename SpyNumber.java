public class SpyNumber {
    public static void main(String[] args) {
        int n=1124;
        int sum=0,prod=1;
        int temp=n;
        for(;temp!=0;temp/=10){
            int d=temp%10;
            sum+=d;
            prod*=d;
        }
        if(sum==prod){
            System.out.println("Spy Number");
        }else{
            System.out.println("Not Spy Number");
        }
    }
}

